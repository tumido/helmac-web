/**
 * Generates registry.ts and JSON path files from paths/*.ts sources.
 * Run: node lib/icons/build-registry.mjs
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..", "..");
const pathsDir = join(__dirname, "paths");
const jsonDir = join(root, "public", "icons");
const outFile = join(__dirname, "registry.ts");

mkdirSync(jsonDir, { recursive: true });

const icons = [];
const tagSet = new Set();

for (const file of readdirSync(pathsDir).sort()) {
    if (!file.endsWith(".ts")) continue;
    const name = file.replace(".ts", "");
    const content = readFileSync(join(pathsDir, file), "utf8");

    const metaMatch = content.match(/^\/\/ @meta (.+)$/m);
    if (!metaMatch) {
        console.warn(`Skipping ${name}: no @meta header`);
        continue;
    }

    const meta = JSON.parse(metaMatch[1]);
    const tags = meta.tags || [meta.category || "Ostatní"];
    tags.forEach((t) => tagSet.add(t));
    icons.push({ name, label: meta.label, tags, author: meta.author });

    // Extract paths array and write as JSON
    const pathsMatch = content.match(
        /const paths\s*=\s*(\[[\s\S]*?\]);\s*\nexport/
    );
    if (pathsMatch) {
        const pathsArray = JSON.parse(
            pathsMatch[1].replace(/\n\s*/g, "").replace(/,\s*]/g, "]")
        );
        writeFileSync(join(jsonDir, `${name}.json`), JSON.stringify(pathsArray));
    }
}

const allTags = [...tagSet].sort();

let ts = `// Auto-generated by build-registry.mjs — do not edit manually\n\n`;
ts += `export interface GameIconMeta {\n`;
ts += `    name: string;\n    label: string;\n    tags: string[];\n    author: string;\n}\n\n`;
ts += `export const GAME_ICONS: GameIconMeta[] = [\n`;
for (const icon of icons) {
    ts += `    { name: "${icon.name}", label: "${icon.label}", tags: ${JSON.stringify(icon.tags)}, author: "${icon.author}" },\n`;
}
ts += `];\n\n`;
ts += `export const ICON_TAGS = [\n`;
for (const t of allTags) {
    ts += `    "${t}",\n`;
}
ts += `] as const;\n\n`;
ts += `export type IconTag = (typeof ICON_TAGS)[number];\n`;

writeFileSync(outFile, ts);
console.log(
    `Generated registry.ts + ${icons.length} JSON files, ${allTags.length} tags`
);
