/**
 * Generates registry.ts from metadata in paths/*.ts files.
 * Run: node lib/icons/build-registry.mjs
 */
import { readdirSync, readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const pathsDir = join(__dirname, "paths");
const outFile = join(__dirname, "registry.ts");

const icons = [];

for (const file of readdirSync(pathsDir).sort()) {
    if (!file.endsWith(".ts")) continue;
    const name = file.replace(".ts", "");
    const content = readFileSync(join(pathsDir, file), "utf8");

    const metaMatch = content.match(/^\/\/ @meta (.+)$/m);
    if (!metaMatch) {
        console.warn(`Skipping ${name}: no @meta header`);
        continue;
    }

    const meta = JSON.parse(metaMatch[1]);
    icons.push({ name, ...meta });
}

const categories = [...new Set(icons.map((i) => i.category))];

let ts = `// Auto-generated by build-registry.mjs — do not edit manually\n\n`;
ts += `export interface GameIconMeta {\n`;
ts += `    name: string;\n    label: string;\n    category: string;\n    author: string;\n}\n\n`;
ts += `export const GAME_ICONS: GameIconMeta[] = [\n`;
for (const icon of icons) {
    ts += `    { name: "${icon.name}", label: "${icon.label}", category: "${icon.category}", author: "${icon.author}" },\n`;
}
ts += `];\n\n`;
ts += `export const ICON_CATEGORIES = [\n`;
for (const c of categories) {
    ts += `    "${c}",\n`;
}
ts += `] as const;\n\n`;
ts += `export type IconCategory = (typeof ICON_CATEGORIES)[number];\n\n`;
ts += `export async function getIconPaths(name: string): Promise<string[]> {\n`;
ts += `    try {\n`;
ts += `        const mod = await import("./paths/" + name);\n`;
ts += `        return mod.default;\n`;
ts += `    } catch {\n`;
ts += `        return [];\n`;
ts += `    }\n`;
ts += `}\n`;

writeFileSync(outFile, ts);
console.log(
    `Generated registry.ts: ${icons.length} icons, ${categories.length} categories`
);
